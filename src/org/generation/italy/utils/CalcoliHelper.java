package org.generation.italy.utils;

public class CalcoliHelper {

	
	public static int somma (int a, int b) {
		return a + b;
	}
	
	public static double sommaD(double a, double b) {
		return a + b;
	}
	
	
	
	public static int divisione(int a, int b) {
		return a / b;
	}
	
	public static double divisioneD(double a, double b) {
		return a / b;
	}
	
	
	
	
	
	public static int moltiplicazione(int a, int b) {
		return a * b;
	}
	public static double moltiplicazioneD(double a, double b) {
		return a * b;
	}
	
	
	
	
	public static int valoreAssoluto;
	public static double valoreAssolutoD;
	
	
	
	
	public static int valoreMinimo;
	public static double valoreMinimoD;
	
	
	
	
	public static int valoreMassimo;
	public static double valoreMassimoD;
	
	
}
